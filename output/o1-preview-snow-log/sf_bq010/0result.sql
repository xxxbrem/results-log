SELECT
  "Product_Name", "Total_Quantity_Sold"
FROM (
  SELECT
    p.value::VARIANT:"v2ProductName"::STRING AS "Product_Name",
    SUM(COALESCE(p.value::VARIANT:"productQuantity"::NUMBER, 0)) AS "Total_Quantity_Sold"
  FROM
    sessions t,
    LATERAL FLATTEN(input => t."hits") h,
    LATERAL FLATTEN(input => h.value::VARIANT:"product") p
  WHERE
    t."fullVisitorId" IN (
      SELECT DISTINCT
        t2."fullVisitorId"
      FROM
        sessions t2,
        LATERAL FLATTEN(input => t2."hits") h2,
        LATERAL FLATTEN(input => h2.value::VARIANT:"product") p2
      WHERE
        p2.value::VARIANT:"v2ProductName"::STRING = 'YouTube Men''s Vintage Henley'
    )
    AND p.value::VARIANT:"v2ProductName"::STRING IS NOT NULL
    AND p.value::VARIANT:"v2ProductName"::STRING <> 'YouTube Men''s Vintage Henley'
  GROUP BY
    p.value::VARIANT:"v2ProductName"::STRING
  ORDER BY
    "Total_Quantity_Sold" DESC NULLS LAST
  LIMIT 1
