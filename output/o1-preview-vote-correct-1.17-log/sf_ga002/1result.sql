users_who_bought_tee AS (
    SELECT DISTINCT
        t."USER_PSEUDO_ID",
        TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'MM') AS "Month_num",
        TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'Month YYYY') AS "Month"
    FROM combined_events t,
         LATERAL FLATTEN(input => t."ITEMS") i
    WHERE t."EVENT_NAME" = 'purchase'
      AND i.value:"item_name"::STRING = 'Google Red Speckled Tee'
      AND i.value:"item_name"::STRING IS NOT NULL
      AND i.value:"item_name"::STRING != '(not set)'
),
other_products AS (
    SELECT
        t."USER_PSEUDO_ID",
        TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'MM') AS "Month_num",
        TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'Month YYYY') AS "Month",
        i.value:"item_name"::STRING AS "Product_Name",
        SUM(i.value:"quantity"::NUMBER) AS "Quantity"
    FROM combined_events t,
         LATERAL FLATTEN(input => t."ITEMS") i
    WHERE t."EVENT_NAME" = 'purchase'
      AND t."USER_PSEUDO_ID" IN (
          SELECT "USER_PSEUDO_ID" FROM users_who_bought_tee
          WHERE "Month_num" = TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'MM')
        )
      AND i.value:"item_name"::STRING IS NOT NULL
      AND i.value:"item_name"::STRING != '(not set)'
      AND i.value:"item_name"::STRING != 'Google Red Speckled Tee'
    GROUP BY
        t."USER_PSEUDO_ID",
        TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'MM'),
        TO_CHAR(TO_DATE(t."EVENT_DATE", 'YYYYMMDD'), 'Month YYYY'),
        i.value:"item_name"::STRING
),
monthly_top_products AS (
    SELECT
        "Month_num",
        "Month",
        "Product_Name",
        SUM("Quantity") AS "Total_Quantity",
        ROW_NUMBER() OVER (
            PARTITION BY "Month_num"
            ORDER BY SUM("Quantity") DESC NULLS LAST
        ) AS rn
    FROM other_products
    GROUP BY
        "Month_num",
        "Month",
        "Product_Name"
)
SELECT
    "Month_num",
    "Month",
    "Product_Name",
    "Total_Quantity" AS "Quantity"
FROM monthly_top_products
WHERE rn = 1
ORDER BY CAST("Month_num" AS INTEGER) NULLS LAST;