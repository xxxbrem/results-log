    SELECT DISTINCT t."fullVisitorId"
    FROM july_data t,
    LATERAL FLATTEN(input => t."hits") h,
    LATERAL FLATTEN(input => h.value:"product") p
    WHERE p.value:"v2ProductName"::STRING = 'YouTube Men\'s Vintage Henley'
),
customer_products AS (
    SELECT p.value:"v2ProductName"::STRING AS "Product_Name",
           SUM(p.value:"productQuantity"::INTEGER) AS "Total_Sales"
    FROM july_data t
    INNER JOIN customers c ON t."fullVisitorId" = c."fullVisitorId"
    , LATERAL FLATTEN(input => t."hits") h
    , LATERAL FLATTEN(input => h.value:"product") p
    WHERE p.value:"v2ProductName"::STRING <> 'YouTube Men\'s Vintage Henley'
    GROUP BY "Product_Name"
)
SELECT "Product_Name", "Total_Sales"
FROM customer_products
ORDER BY "Total_Sales" DESC NULLS LAST
