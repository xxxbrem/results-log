SELECT p.value:"v2ProductName"::STRING AS "Product_Name",
       SUM(COALESCE(TRY_TO_NUMBER(p.value:"productQuantity"::STRING), 1)) AS "Total_Quantity_Sold"
FROM all_sessions t,
     LATERAL FLATTEN(input => t."hits") h,
     LATERAL FLATTEN(input => h.value:"product") p
WHERE t."fullVisitorId" IN (
    SELECT DISTINCT t_sub."fullVisitorId"
    FROM all_sessions t_sub,
         LATERAL FLATTEN(input => t_sub."hits") h_sub,
         LATERAL FLATTEN(input => h_sub.value:"product") p_sub
    WHERE p_sub.value:"v2ProductName"::STRING = 'YouTube Men''s Vintage Henley'
      AND h_sub.value:"eCommerceAction":"action_type"::INTEGER = 6
)
  AND p.value:"v2ProductName"::STRING != 'YouTube Men''s Vintage Henley'
  AND h.value:"eCommerceAction":"action_type"::INTEGER = 6
GROUP BY "Product_Name"
ORDER BY "Total_Quantity_Sold" DESC NULLS LAST
LIMIT 1;