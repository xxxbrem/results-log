SELECT
   (
       SELECT COUNT(*) 
       FROM THELOOK_ECOMMERCE.THELOOK_ECOMMERCE.USERS
       WHERE "created_at" BETWEEN 1546300800000000 AND 1651276800000000
       AND "age" = (
           SELECT MAX("age") 
           FROM THELOOK_ECOMMERCE.THELOOK_ECOMMERCE.USERS
           WHERE "created_at" BETWEEN 1546300800000000 AND 1651276800000000
       )
   ) -
   (
       SELECT COUNT(*) 
       FROM THELOOK_ECOMMERCE.THELOOK_ECOMMERCE.USERS
       WHERE "created_at" BETWEEN 1546300800000000 AND 1651276800000000
       AND "age" = (
           SELECT MIN("age") 
           FROM THELOOK_ECOMMERCE.THELOOK_ECOMMERCE.USERS
           WHERE "created_at" BETWEEN 1546300800000000 AND 1651276800000000
       )
   ) AS "age_count_difference";