    ROUND(AVG(session_count), 4) AS "average_engaged_sessions_per_user"
FROM (
    SELECT
        "USER_PSEUDO_ID",
        COUNT(DISTINCT "GA_SESSION_ID") AS session_count
    FROM params
    WHERE "SESSION_ENGAGED" = '1'
    GROUP BY "USER_PSEUDO_ID"
) AS session_counts;