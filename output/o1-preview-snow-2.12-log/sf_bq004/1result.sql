visitors_with_youtube_purchases AS (
  SELECT DISTINCT t."fullVisitorId"
  FROM all_sessions t,
       LATERAL FLATTEN(INPUT => t."hits") h,
       LATERAL FLATTEN(INPUT => h.value:"product") p
  WHERE p.value:"v2ProductName"::STRING ILIKE '%YouTube%'
    AND h.value:"eCommerceAction"."action_type"::STRING = '6'
),
purchases_by_visitors AS (
  SELECT p.value:"v2ProductName"::STRING AS "productName",
         SUM(COALESCE(p.value:"productQuantity"::INTEGER, 1)) AS "totalQuantity"
  FROM all_sessions t,
       LATERAL FLATTEN(INPUT => t."hits") h,
       LATERAL FLATTEN(INPUT => h.value:"product") p
  WHERE t."fullVisitorId" IN (SELECT "fullVisitorId" FROM visitors_with_youtube_purchases)
    AND p.value:"v2ProductName"::STRING NOT ILIKE '%YouTube%'
    AND h.value:"eCommerceAction"."action_type"::STRING = '6'
  GROUP BY p.value:"v2ProductName"::STRING
)
SELECT "productName", "totalQuantity"
FROM purchases_by_visitors
ORDER BY "totalQuantity" DESC NULLS LAST
LIMIT 1;