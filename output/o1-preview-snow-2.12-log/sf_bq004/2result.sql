youtube_buyers AS (
    SELECT DISTINCT t."fullVisitorId"
    FROM july_sessions t,
    LATERAL FLATTEN(input => t."hits") h,
    LATERAL FLATTEN(input => h.value:"product") p
    WHERE h.value:"eCommerceAction":"action_type"::STRING = '6'
      AND p.value:"v2ProductName"::STRING ILIKE '%YouTube%'
)
SELECT
    p.value:"v2ProductName"::STRING AS "product_name",
    SUM(p.value:"productQuantity"::INTEGER) AS "total_quantity"
FROM july_sessions t,
LATERAL FLATTEN(input => t."hits") h,
LATERAL FLATTEN(input => h.value:"product") p
WHERE t."fullVisitorId" IN (SELECT "fullVisitorId" FROM youtube_buyers)
  AND h.value:"eCommerceAction":"action_type"::STRING = '6'
  AND p.value:"v2ProductName"::STRING NOT ILIKE '%YouTube%'
GROUP BY "product_name"
ORDER BY "total_quantity" DESC NULLS LAST
LIMIT 1;