SELECT "user_id", "tags"
FROM (
    SELECT COMMENTS."user_id", POSTS."tags", COMMENTS."creation_date"
    FROM "STACKOVERFLOW"."STACKOVERFLOW"."COMMENTS" COMMENTS
    JOIN "STACKOVERFLOW"."STACKOVERFLOW"."STACKOVERFLOW_POSTS" POSTS
        ON COMMENTS."post_id" = POSTS."id"

    UNION ALL

    SELECT ANSWERS."owner_user_id" AS "user_id", QUESTIONS."tags", ANSWERS."creation_date"
    FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" ANSWERS
    JOIN "STACKOVERFLOW"."STACKOVERFLOW"."STACKOVERFLOW_POSTS" QUESTIONS
        ON ANSWERS."parent_id" = QUESTIONS."id"

    UNION ALL

    SELECT QUESTIONS."owner_user_id" AS "user_id", QUESTIONS."tags", QUESTIONS."creation_date"
    FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" QUESTIONS
)
WHERE "user_id" BETWEEN 16712208 AND 18712208
  AND "creation_date" BETWEEN 1561939200000000 AND 1577836799000000;