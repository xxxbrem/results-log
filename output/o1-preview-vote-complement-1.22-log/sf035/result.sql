SELECT "APP_GROUP_ID", DATE(TO_TIMESTAMP_LTZ("TIME")) AS "Start_Day", COUNT(DISTINCT "USER_ID") AS "Unique_Users"
FROM BRAZE_USER_EVENT_DEMO_DATASET.PUBLIC.USERS_BEHAVIORS_APP_SESSIONSTART_VIEW
WHERE TO_TIMESTAMP_LTZ("TIME") >= '2023-06-01' AND TO_TIMESTAMP_LTZ("TIME") <= '2023-06-07'
GROUP BY "APP_GROUP_ID", DATE(TO_TIMESTAMP_LTZ("TIME"))
ORDER BY "Start_Day", "APP_GROUP_ID";