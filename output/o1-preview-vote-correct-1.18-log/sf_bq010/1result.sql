customers_who_bought_henley AS (
    SELECT DISTINCT t."fullVisitorId"
    FROM july_sessions t
    , LATERAL FLATTEN(input => t."hits") h
    , LATERAL FLATTEN(input => h.value:"product") p
    WHERE LOWER(p.value:"v2ProductName"::STRING) = 'youtube men''s vintage henley'
      AND (
        h.value:"type"::STRING = 'TRANSACTION' OR 
        h.value:"type"::STRING = 'ITEM' OR
        h.value:"eCommerceAction":"action_type"::INTEGER = 6
      )
)
SELECT
    p.value:"v2ProductName"::STRING AS "ProductName",
    SUM(COALESCE(p.value:"productQuantity"::INTEGER, 1)) AS "TotalUnitsSold"
FROM july_sessions t
JOIN customers_who_bought_henley c
  ON t."fullVisitorId" = c."fullVisitorId"
,LATERAL FLATTEN(input => t."hits") h
,LATERAL FLATTEN(input => h.value:"product") p
WHERE LOWER(p.value:"v2ProductName"::STRING) != 'youtube men''s vintage henley'
  AND (
    h.value:"type"::STRING = 'TRANSACTION' OR 
    h.value:"type"::STRING = 'ITEM' OR
    h.value:"eCommerceAction":"action_type"::INTEGER = 6
  )
GROUP BY "ProductName"
ORDER BY "TotalUnitsSold" DESC NULLS LAST
