SELECT p.value:"v2ProductName"::STRING AS "product_name", COUNT(*) AS "total_sales"
FROM all_sessions t
CROSS JOIN LATERAL FLATTEN(input => t."hits") f
CROSS JOIN LATERAL FLATTEN(input => f.value:"product") p
WHERE t."fullVisitorId" IN (
  SELECT DISTINCT t2."fullVisitorId"
  FROM all_sessions t2
  CROSS JOIN LATERAL FLATTEN(input => t2."hits") f2
  CROSS JOIN LATERAL FLATTEN(input => f2.value:"product") p2
  WHERE p2.value:"v2ProductName"::STRING = 'YouTube Men''s Vintage Henley'
    AND f2.value:"eCommerceAction":"action_type"::STRING = '6'
)
AND p.value:"v2ProductName"::STRING != 'YouTube Men''s Vintage Henley'
AND f.value:"eCommerceAction":"action_type"::STRING = '6'
GROUP BY p.value:"v2ProductName"::STRING
ORDER BY "total_sales" DESC NULLS LAST
LIMIT 1;