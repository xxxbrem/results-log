SELECT "user_id", "tags"
FROM (
    SELECT
        "owner_user_id" AS "user_id",
        "tags"
    FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS"
    WHERE "owner_user_id" BETWEEN 16712208 AND 18712208
        AND "creation_date" BETWEEN 1561939200000000 AND 1577836799000000

    UNION ALL

    SELECT
        A."owner_user_id" AS "user_id",
        Q."tags"
    FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" A
    JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" Q
        ON A."parent_id" = Q."id"
    WHERE A."owner_user_id" BETWEEN 16712208 AND 18712208
        AND A."creation_date" BETWEEN 1561939200000000 AND 1577836799000000

    UNION ALL

    SELECT
        C."user_id",
        COALESCE(Q."tags", PQ."tags") AS "tags"
    FROM "STACKOVERFLOW"."STACKOVERFLOW"."COMMENTS" C
    LEFT JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" Q
        ON C."post_id" = Q."id"
    LEFT JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" A
        ON C."post_id" = A."id"
    LEFT JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" PQ
        ON A."parent_id" = PQ."id"
    WHERE C."user_id" BETWEEN 16712208 AND 18712208
        AND C."creation_date" BETWEEN 1561939200000000 AND 1577836799000000
) AS CombinedResult;