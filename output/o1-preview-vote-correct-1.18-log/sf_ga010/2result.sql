        UNION ALL
        SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20201231"
    ) t,
    LATERAL FLATTEN(input => t."EVENT_PARAMS") ep
    WHERE t."EVENT_NAME" = 'session_start' AND ep.value:"key"::STRING = 'ga_session_id'
) sessions
GROUP BY "Channel"
