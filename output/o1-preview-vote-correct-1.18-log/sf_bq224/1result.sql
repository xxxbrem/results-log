    FROM events e
    INNER JOIN "GITHUB_REPOS_DATE"."GITHUB_REPOS"."LICENSES" l
    ON e."repo_name" = l."repo_name"
    WHERE e."type" IN ('ForkEvent', 'IssuesEvent', 'WatchEvent')
)
