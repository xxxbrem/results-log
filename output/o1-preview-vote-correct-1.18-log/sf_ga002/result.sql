customers_per_month AS (
  SELECT DISTINCT SUBSTRING(t."EVENT_DATE", 1, 6) AS "Month", t."USER_PSEUDO_ID"
  FROM all_events t
  CROSS JOIN LATERAL FLATTEN(input => t."ITEMS") f
  WHERE t."EVENT_NAME" = 'purchase'
    AND f.value:"item_name"::STRING = 'Google Red Speckled Tee'
),
customer_purchases AS (
  SELECT SUBSTRING(t."EVENT_DATE", 1, 6) AS "Month",
         f.value:"item_name"::STRING AS "Product_name",
         SUM(f.value:"quantity"::NUMBER) AS "Total_quantity"
  FROM all_events t
  JOIN customers_per_month c
    ON t."USER_PSEUDO_ID" = c."USER_PSEUDO_ID"
   AND SUBSTRING(t."EVENT_DATE", 1, 6) = c."Month"
  CROSS JOIN LATERAL FLATTEN(input => t."ITEMS") f
  WHERE t."EVENT_NAME" = 'purchase'
    AND f.value:"item_name"::STRING <> 'Google Red Speckled Tee'
  GROUP BY SUBSTRING(t."EVENT_DATE", 1, 6), f.value:"item_name"::STRING
),
ranked_products AS (
  SELECT "Month",
         "Product_name",
         "Total_quantity",
         ROW_NUMBER() OVER (PARTITION BY "Month" ORDER BY "Total_quantity" DESC NULLS LAST, "Product_name") AS "rn"
  FROM customer_purchases
)
SELECT CASE "Month"
