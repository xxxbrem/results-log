SELECT T2."country_name", ROUND(T1."net_migration", 4) AS "Net_Migration_Rate"
FROM CENSUS_BUREAU_INTERNATIONAL.CENSUS_BUREAU_INTERNATIONAL.BIRTH_DEATH_GROWTH_RATES T1
JOIN CENSUS_BUREAU_INTERNATIONAL.CENSUS_BUREAU_INTERNATIONAL.COUNTRY_NAMES_AREA T2
ON T1."country_code" = T2."country_code"
WHERE T1."year" = 2017
  AND T2."country_area" > 500
  AND T1."net_migration" IS NOT NULL
ORDER BY T1."net_migration" DESC NULLS LAST
LIMIT 3;