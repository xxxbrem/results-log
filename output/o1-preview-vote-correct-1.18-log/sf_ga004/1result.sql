  SELECT DISTINCT "USER_PSEUDO_ID"
  FROM all_events
),
user_purchases AS (
  SELECT DISTINCT "USER_PSEUDO_ID"
  FROM all_events
  WHERE "EVENT_NAME" = 'purchase'
),
user_pageviews AS (
  SELECT "USER_PSEUDO_ID", COUNT(*) AS "pageviews"
  FROM all_events
  WHERE "EVENT_NAME" = 'page_view'
  GROUP BY "USER_PSEUDO_ID"
),
user_pageviews_full AS (
  SELECT au."USER_PSEUDO_ID", COALESCE(upv."pageviews", 0) AS "pageviews"
  FROM all_users au
  LEFT JOIN user_pageviews upv ON au."USER_PSEUDO_ID" = upv."USER_PSEUDO_ID"
),
user_labels AS (
  SELECT upf."USER_PSEUDO_ID",
         CASE WHEN up."USER_PSEUDO_ID" IS NOT NULL THEN 1 ELSE 0 END AS "is_purchaser",
         upf."pageviews"
  FROM user_pageviews_full upf
  LEFT JOIN user_purchases up ON upf."USER_PSEUDO_ID" = up."USER_PSEUDO_ID"
)
SELECT
  ROUND(ABS(AVG(CASE WHEN "is_purchaser" = 1 THEN "pageviews" END) -
            AVG(CASE WHEN "is_purchaser" = 0 THEN "pageviews" END)), 4) AS "average_pageviews_difference"
FROM user_labels;