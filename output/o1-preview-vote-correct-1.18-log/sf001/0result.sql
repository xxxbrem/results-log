SELECT
    "POSTAL_CODE",
    "DATE_VALID_STD",
    ROUND("TOT_SNOWFALL_IN", 4) AS "Snowfall Amount"
FROM
    "GLOBAL_WEATHER__CLIMATE_DATA_FOR_BI"."STANDARD_TILE"."HISTORY_DAY"
WHERE
    "COUNTRY" = 'US'
    AND "TOT_SNOWFALL_IN" > 6
    AND "DATE_VALID_STD" BETWEEN '2023-01-15' AND '2023-01-21'
ORDER BY
    "DATE_VALID_STD" ASC,
    "POSTAL_CODE" ASC;