    , LATERAL FLATTEN(input => f.value:"product") p
    WHERE p.value:"v2ProductName"::STRING = 'YouTube Men''s Vintage Henley'
      AND f.value:"eCommerceAction":"action_type"::INTEGER = 6  -- Purchase action
)
SELECT p.value:"v2ProductName"::STRING AS "Product_Name",
       SUM(COALESCE(p.value:"productQuantity"::INTEGER, 1)) AS "Total_Sold"
FROM combined_sessions t
JOIN customers c ON t."fullVisitorId" = c."fullVisitorId"
, LATERAL FLATTEN(input => t."hits") f
, LATERAL FLATTEN(input => f.value:"product") p
WHERE p.value:"v2ProductName"::STRING <> 'YouTube Men''s Vintage Henley'
  AND f.value:"eCommerceAction":"action_type"::INTEGER = 6  -- Purchase action
GROUP BY "Product_Name"
ORDER BY "Total_Sold" DESC NULLS LAST
