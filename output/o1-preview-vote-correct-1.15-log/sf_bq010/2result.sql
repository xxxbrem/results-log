customers AS (
    SELECT DISTINCT s."fullVisitorId"
    FROM sessions s
    , LATERAL FLATTEN(input => s."hits") h
    , LATERAL FLATTEN(input => h.value:"product") p
    WHERE p.value:"v2ProductName"::STRING = 'YouTube Men''s Vintage Henley'
      AND TRY_TO_NUMBER(h.value:"eCommerceAction"."action_type"::STRING) = 6
)
SELECT p.value:"v2ProductName"::STRING AS "product_name",
       SUM(TRY_TO_NUMBER(p.value:"productQuantity"::STRING)) AS "units_sold"
FROM sessions s
, LATERAL FLATTEN(input => s."hits") h
, LATERAL FLATTEN(input => h.value:"product") p
WHERE s."fullVisitorId" IN (SELECT "fullVisitorId" FROM customers)
  AND p.value:"v2ProductName"::STRING != 'YouTube Men''s Vintage Henley'
  AND TRY_TO_NUMBER(h.value:"eCommerceAction"."action_type"::STRING) = 6
GROUP BY "product_name"
ORDER BY "units_sold" DESC NULLS LAST
LIMIT 1;