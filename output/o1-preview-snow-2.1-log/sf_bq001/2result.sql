feb_sessions AS (
  SELECT
    "fullVisitorId",
    TO_TIMESTAMP("visitStartTime") AS "visitStartTs",
    "totals"::VARIANT:"transactions"::NUMBER AS "transactions",
    "device"::VARIANT:"deviceCategory"::STRING AS "deviceCategory"
  FROM all_feb_tables
),
first_transactions AS (
  SELECT
    "fullVisitorId",
    MIN("visitStartTs") AS "first_transaction_time"
  FROM feb_sessions
  WHERE "transactions" > 0
  GROUP BY "fullVisitorId"
),
first_visits AS (
  SELECT
    "fullVisitorId",
    MIN("visitStartTs") AS "first_visit_time"
  FROM feb_sessions
  GROUP BY "fullVisitorId"
)
SELECT
  t."fullVisitorId",
  DATEDIFF('day', f."first_visit_time", t."first_transaction_time") AS "days_between",
  s."deviceCategory"
FROM first_transactions t
JOIN first_visits f ON t."fullVisitorId" = f."fullVisitorId"
JOIN feb_sessions s ON t."fullVisitorId" = s."fullVisitorId"
  AND t."first_transaction_time" = s."visitStartTs"
  AND s."transactions" > 0
ORDER BY t."fullVisitorId";