SELECT "User_ID", "Tags"
FROM (
    SELECT 
        "owner_user_id" AS "User_ID", 
        "tags" AS "Tags"
    FROM 
        "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS"
    WHERE 
        "owner_user_id" BETWEEN 16712208 AND 18712208
        AND "creation_date" BETWEEN 1561939200000000 AND 1577836799000000

    UNION ALL

    SELECT 
        "POSTS_ANSWERS"."owner_user_id" AS "User_ID", 
        "QUESTIONS"."tags" AS "Tags"
    FROM 
        "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" AS "POSTS_ANSWERS"
    INNER JOIN 
        "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" AS "QUESTIONS"
        ON "POSTS_ANSWERS"."parent_id" = "QUESTIONS"."id"
    WHERE 
        "POSTS_ANSWERS"."owner_user_id" BETWEEN 16712208 AND 18712208
        AND "POSTS_ANSWERS"."creation_date" BETWEEN 1561939200000000 AND 1577836799000000

    UNION ALL

    SELECT 
        "COMMENTS"."user_id" AS "User_ID", 
        "QUESTIONS"."tags" AS "Tags"
    FROM 
        "STACKOVERFLOW"."STACKOVERFLOW"."COMMENTS" AS "COMMENTS"
    INNER JOIN 
        "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" AS "QUESTIONS"
        ON "COMMENTS"."post_id" = "QUESTIONS"."id"
    WHERE 
        "COMMENTS"."user_id" BETWEEN 16712208 AND 18712208
        AND "COMMENTS"."creation_date" BETWEEN 1561939200000000 AND 1577836799000000

    UNION ALL

    SELECT 
        "COMMENTS"."user_id" AS "User_ID", 
        "QUESTIONS"."tags" AS "Tags"
    FROM 
        "STACKOVERFLOW"."STACKOVERFLOW"."COMMENTS" AS "COMMENTS"
    INNER JOIN 
        "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" AS "ANSWERS"
        ON "COMMENTS"."post_id" = "ANSWERS"."id"
    INNER JOIN 
        "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" AS "QUESTIONS"
        ON "ANSWERS"."parent_id" = "QUESTIONS"."id"
    WHERE 
        "COMMENTS"."user_id" BETWEEN 16712208 AND 18712208
        AND "COMMENTS"."creation_date" BETWEEN 1561939200000000 AND 1577836799000000
) AS "All_Posts";