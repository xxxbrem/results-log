SELECT "owner_user_id" AS "User_ID", "tags"
FROM STACKOVERFLOW.STACKOVERFLOW.POSTS_QUESTIONS
WHERE "owner_user_id" BETWEEN 16712208 AND 18712208
  AND "creation_date" >= 1561939200000000.0000 AND "creation_date" <= 1577750399999999.9999

UNION ALL

SELECT ANSWERS."owner_user_id" AS "User_ID", QUESTIONS."tags"
FROM STACKOVERFLOW.STACKOVERFLOW.POSTS_ANSWERS AS ANSWERS
JOIN STACKOVERFLOW.STACKOVERFLOW.POSTS_QUESTIONS AS QUESTIONS
  ON ANSWERS."parent_id" = QUESTIONS."id"
WHERE ANSWERS."owner_user_id" BETWEEN 16712208 AND 18712208
  AND ANSWERS."creation_date" >= 1561939200000000.0000 AND ANSWERS."creation_date" <= 1577750399999999.9999

UNION ALL

SELECT COMMENTS."user_id" AS "User_ID", QUESTIONS."tags"
FROM STACKOVERFLOW.STACKOVERFLOW.COMMENTS AS COMMENTS
JOIN STACKOVERFLOW.STACKOVERFLOW.STACKOVERFLOW_POSTS AS POSTS
  ON COMMENTS."post_id" = POSTS."id"
JOIN STACKOVERFLOW.STACKOVERFLOW.POSTS_QUESTIONS AS QUESTIONS
  ON (
    (POSTS."post_type_id" = 1 AND POSTS."id" = QUESTIONS."id")
    OR
    (POSTS."post_type_id" = 2 AND POSTS."parent_id" = QUESTIONS."id")
  )
WHERE COMMENTS."user_id" BETWEEN 16712208 AND 18712208
  AND COMMENTS."creation_date" >= 1561939200000000.0000 AND COMMENTS."creation_date" <= 1577750399999999.9999;