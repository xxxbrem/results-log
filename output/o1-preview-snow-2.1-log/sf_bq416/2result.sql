SELECT
  "BLOCK_NUMBER",
  (PARSE_JSON("ARGS")[0])::STRING AS "Source_Address",
  (PARSE_JSON("ARGS")[1])::STRING AS "Destination_Address",
  ROUND((PARSE_JSON("ARGS")[2])::NUMBER, 4) AS "Transfer_Amount"
FROM
  "GOOG_BLOCKCHAIN"."GOOG_BLOCKCHAIN_ARBITRUM_ONE_US"."DECODED_EVENTS"
WHERE
  "ADDRESS" = '0xfd086bc7cd5c481dcc9c85ebe478a1c0b69fcbb9' AND
  "EVENT_HASH" = '0xddf252ad1be2c89b69c2b068fc378daa952ba7f163c4a11628f55a4df523b3ef'
ORDER BY
  (PARSE_JSON("ARGS")[2])::NUMBER DESC NULLS LAST
LIMIT 3;