SELECT
    EXTRACT(year FROM "DATE_OF_LOSS") AS "Year",
    SUM(TRY_TO_DOUBLE("AMOUNT_PAID_ON_BUILDING_CLAIM")) AS "Total_Building_Damage_Amount",
    SUM(TRY_TO_DOUBLE("AMOUNT_PAID_ON_CONTENTS_CLAIM")) AS "Total_Contents_Damage_Amount"
FROM
    "WEATHER__ENVIRONMENT"."CYBERSYN"."FEMA_NATIONAL_FLOOD_INSURANCE_PROGRAM_CLAIM_INDEX"
WHERE
    "DATE_OF_LOSS" BETWEEN '2010-01-01' AND '2019-12-31'
    AND "COUNTY_GEO_ID" IN ('geoId/36005', 'geoId/36047', 'geoId/36061', 'geoId/36081', 'geoId/36085')
GROUP BY
    "Year"
ORDER BY
    "Year";