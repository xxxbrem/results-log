),
sessions AS (
    SELECT
        CASE
            WHEN LOWER(TRIM(("TRAFFIC_SOURCE":"medium")::VARCHAR)) = 'cpc' THEN 'Paid Search'
            WHEN LOWER(TRIM(("TRAFFIC_SOURCE":"medium")::VARCHAR)) = 'organic' THEN 'Organic Search'
            WHEN LOWER(TRIM(("TRAFFIC_SOURCE":"medium")::VARCHAR)) = 'referral' THEN 'Referral'
            WHEN LOWER(TRIM(("TRAFFIC_SOURCE":"medium")::VARCHAR)) IN ('(not set)', '(none)', '') 
                 OR LOWER(TRIM(("TRAFFIC_SOURCE":"source")::VARCHAR)) = '(direct)' 
                 OR ("TRAFFIC_SOURCE":"medium") IS NULL THEN 'Direct'
            ELSE 'Unassigned'
        END AS "Channel",
        "USER_PSEUDO_ID"
    FROM events
    WHERE "EVENT_NAME" = 'session_start'
)
SELECT "Channel", COUNT(DISTINCT "USER_PSEUDO_ID") AS "Number_of_Sessions"
FROM sessions
GROUP BY "Channel"
ORDER BY "Number_of_Sessions" DESC NULLS LAST, "Channel" ASC
LIMIT 1 OFFSET 3;