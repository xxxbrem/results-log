SELECT p."user", SUM(m."NMATCH") AS "total_clicks"
FROM (
    SELECT "user", "id" FROM ECLIPSE_MEGAMOVIE.ECLIPSE_MEGAMOVIE."PHOTOS_V_0_1"
    UNION ALL
    SELECT "user", "id" FROM ECLIPSE_MEGAMOVIE.ECLIPSE_MEGAMOVIE."PHOTOS_V_0_2"
    UNION ALL
    SELECT "user", "id" FROM ECLIPSE_MEGAMOVIE.ECLIPSE_MEGAMOVIE."PHOTOS_V_0_3"
) AS p
JOIN ECLIPSE_MEGAMOVIE.ECLIPSE_MEGAMOVIE."ASTROMETRY_MATCH_V_0_4" AS m
ON p."id" = m."id"
GROUP BY p."user"
ORDER BY "total_clicks" DESC NULLS LAST
LIMIT 1;