SELECT "user_id", "tags" FROM (
  SELECT "owner_user_id" AS "user_id", "tags"
  FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS"
  WHERE "owner_user_id" BETWEEN 16712208 AND 18712208
    AND "creation_date" BETWEEN 1561939200000000 AND 1577836799999999

  UNION ALL

  SELECT a."owner_user_id" AS "user_id", q."tags"
  FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" a
  JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" q
    ON a."parent_id" = q."id"
  WHERE a."owner_user_id" BETWEEN 16712208 AND 18712208
    AND a."creation_date" BETWEEN 1561939200000000 AND 1577836799999999

  UNION ALL

  SELECT c."user_id", COALESCE(q."tags", qa."tags") AS "tags"
  FROM "STACKOVERFLOW"."STACKOVERFLOW"."COMMENTS" c
  LEFT JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" q
    ON c."post_id" = q."id"
  LEFT JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS" a
    ON c."post_id" = a."id"
  LEFT JOIN "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_QUESTIONS" qa
    ON a."parent_id" = qa."id"
  WHERE c."user_id" BETWEEN 16712208 AND 18712208
    AND c."creation_date" BETWEEN 1561939200000000 AND 1577836799999999
);