SELECT EXTRACT(YEAR FROM "DATE_OF_LOSS") AS "Year",
       ROUND(SUM("AMOUNT_PAID_ON_BUILDING_CLAIM"), 4) AS "Total_Building_Damage_Amount",
       ROUND(SUM("AMOUNT_PAID_ON_CONTENTS_CLAIM"), 4) AS "Total_Contents_Damage_Amount"
FROM WEATHER__ENVIRONMENT.CYBERSYN.FEMA_NATIONAL_FLOOD_INSURANCE_PROGRAM_CLAIM_INDEX
WHERE "COUNTY_GEO_ID" IN ('geoId/36005', 'geoId/36047', 'geoId/36061', 'geoId/36081', 'geoId/36085')
  AND "DATE_OF_LOSS" BETWEEN '2010-01-01' AND '2019-12-31'
GROUP BY "Year"
ORDER BY "Year";