SELECT
  "BLOCK_NUMBER",
  REPLACE(PARSE_JSON("ARGS")[0]::STRING, '"', '') AS "Source_Address",
  REPLACE(PARSE_JSON("ARGS")[1]::STRING, '"', '') AS "Destination_Address",
  ROUND(TRY_TO_NUMBER(REPLACE(PARSE_JSON("ARGS")[2]::STRING, '"', '')), 4) AS "Transfer_Amount"
FROM "GOOG_BLOCKCHAIN"."GOOG_BLOCKCHAIN_ARBITRUM_ONE_US"."DECODED_EVENTS"
WHERE "ADDRESS" = LOWER('0xfd086bc7cd5c481dcc9c85ebe478a1c0b69fcbb9')
  AND "EVENT_HASH" = LOWER('0xddf252ad1be2c89b69c2b068fc378daa952ba7f163c4a11628f55a4df523b3ef')
ORDER BY "Transfer_Amount" DESC NULLS LAST
LIMIT 3;