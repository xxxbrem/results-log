SELECT
    CASE
        WHEN "QUADKEY" LIKE '12020210%' THEN 'Amsterdam'
        WHEN "QUADKEY" LIKE '12020211%' THEN 'Rotterdam'
    END AS "City",
    "CLASS",
    "SUBCLASS",
    ROUND(SUM(CAST("LENGTH_M" AS FLOAT)), 4) AS "Total_Road_Length_meters"
FROM NETHERLANDS_OPEN_MAP_DATA.NETHERLANDS.V_ROAD
WHERE
    "QUADKEY" LIKE '12020210%' OR "QUADKEY" LIKE '12020211%'
GROUP BY
    "City", "CLASS", "SUBCLASS";