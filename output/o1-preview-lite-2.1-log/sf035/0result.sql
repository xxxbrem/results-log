SELECT
     "APP_GROUP_ID",
     DATE(TO_TIMESTAMP("TIME")) AS "Start_Day",
     COUNT(DISTINCT "USER_ID") AS "Unique_Users"
FROM
     BRAZE_USER_EVENT_DEMO_DATASET.PUBLIC.USERS_BEHAVIORS_APP_SESSIONSTART_VIEW
WHERE
     DATE(TO_TIMESTAMP("TIME")) BETWEEN '2023-06-01' AND '2023-06-07'
GROUP BY
     "APP_GROUP_ID", DATE(TO_TIMESTAMP("TIME"))
ORDER BY
     "APP_GROUP_ID", "Start_Day";